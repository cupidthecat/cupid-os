static int eq_bytes(const uint8_t *a, const uint8_t *b, uint32_t n) {
    uint32_t i;
    for (i = 0; i < n; i++) if (a[i] != b[i]) return 0;
    return 1;
}

static void must(int ok, const char *name) {
    if (!ok) {
        serial_printf("[tls-selftest] FAIL: %s\n", name);
        kernel_panic("tls selftest: %s", name);
    }
    serial_printf("[tls-selftest] ok: %s\n", name);
}

