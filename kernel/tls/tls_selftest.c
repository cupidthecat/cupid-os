/* Big integer import, export, and modular exponentiation */

static void test_bigint(void) {
    static const uint8_t base_be[1] = { 0x04 };
    static const uint8_t exponent_be[1] = { 0x0d };
    static const uint8_t modulus_be[2] = { 0x01, 0xf1 };
    static const uint8_t want_modexp[2] = { 0x01, 0xbd };
    static const uint8_t encoded[4] = { 0xde, 0xad, 0xbe, 0xef };
    bn_t base, modulus, result;
    uint8_t out[4];
    int ok;

    ok = bn_from_be(&base, base_be, sizeof(base_be)) == 0 &&
         bn_from_be(&modulus, modulus_be, sizeof(modulus_be)) == 0;
    if (ok) {
        bn_modexp(&result, &base, exponent_be, sizeof(exponent_be), &modulus);
        bn_to_be(out, 2u, &result);
    }
    must(ok && eq_bytes(out, want_modexp, 2u),
         "bigint 4^13 mod 497 == 445");

    ok = bn_from_be(&result, encoded, sizeof(encoded)) == 0;
    if (ok) bn_to_be(out, sizeof(out), &result);
    must(ok && eq_bytes(out, encoded, sizeof(encoded)),
         "bigint big-endian round trip");
}

